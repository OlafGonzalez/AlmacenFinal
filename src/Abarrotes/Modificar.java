     public void articulos(){
       DefaultTableModel articulos = new DefaultTableModel();
        ResultSet rs = con.getTable("select idArticulo,nombre,categoria from Articulo where eliminar = 1");
        articulos.setColumnIdentifiers(new Object[]{"ID","Nombre","categoria"});
        try {
            while(rs.next()){
                articulos.addRow(new Object[]{rs.getString("idArticulo"),rs.getString("nombre"),rs.getString("categoria")});
            }
            jt_lista.setModel(articulos);
        } catch (Exception e) {
            System.out.println(e);
        }
    }
 
    public void modi() throws SQLException {
      
        Conexion con = this.con;
    
        
        try {
            String consulta = "select categoria from Articulo group by categoria;";
           st = con.obtenerConexion().prepareStatement(consulta);
           res = st.executeQuery();
           
            while (res.next()) {                
                cmb_categoria.addItem(res.getString("categoria"));
            }
            res.close();
            
        } catch (SQLException e) {
            
        }
      
        
    }
 
